package condicionales;

/**
 *
 * @author cubes
 */
public class Condicionales {

    public static void main(String[] args) {
        
        /*
         * Los condicionales nos permiten tomar decisiones en el codigo,
         * de esta forma podemos decirle al codigo que debe hacer en situaciones
         * especificas
         *
         * Un ejemplo de un condicional puede ser el siguiente
         *
         * if --> Es una palabra reservada para las condiciones
         *
         * () --> Lo que se encuentra dentro de los parentesis es
         * la condicion, debera ser true o false
         */
        
        // Ejemplo de condicional simple
        if (1 == 1){
            System.out.println("Este condiciona es verdadero");
        }
        
        
    }
    
}
